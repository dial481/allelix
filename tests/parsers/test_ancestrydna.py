class TestEdgeCases:
    def test_header_only_file_yields_zero_variants(
        self, parser: AncestryDNAParser, tmp_path: Path
    ) -> None:
        f = _write(
            tmp_path,
            "#AncestryDNA raw data download\n#\nrsid\tchromosome\tposition\tallele1\tallele2\n",
        )
        assert parser.can_parse(f) is True
        assert list(parser.parse(f)) == []

    def test_single_zero_allele_is_partial_no_call(
        self, parser: AncestryDNAParser, tmp_path: Path
    ) -> None:
        """One allele 0 and one allele A → partial no-call."""
        f = _write(
            tmp_path,
            "#AncestryDNA\nrsid\tchromosome\tposition\tallele1\tallele2\nrs1\t1\t100\t0\tA\n",
        )
        variants = list(parser.parse(f))
        assert len(variants) == 1
        assert variants[0].allele1 == "-"
        assert variants[0].allele2 == "A"
        assert variants[0].is_no_call


