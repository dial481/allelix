class TestEdgeCases:
    def test_header_only_file_yields_zero_variants(
        self, parser: TwentyThreeAndMeParser, tmp_path: Path
    ) -> None:
        f = _write(
            tmp_path,
            "# This data file generated by 23andMe\n# rsid\tchromosome\tposition\tgenotype\n",
        )
        assert parser.can_parse(f) is True
        assert list(parser.parse(f)) == []

    def test_empty_genotype_is_no_call(
        self, parser: TwentyThreeAndMeParser, tmp_path: Path
    ) -> None:
        f = _write(
            tmp_path,
            "# 23andMe\nrs1\t1\t100\t\n",
        )
        variants = list(parser.parse(f))
        assert len(variants) == 1
        assert variants[0].is_no_call

    def test_three_char_genotype_is_no_call(
        self, parser: TwentyThreeAndMeParser, tmp_path: Path
    ) -> None:
        f = _write(
            tmp_path,
            "# 23andMe\nrs1\t1\t100\tABC\n",
        )
        variants = list(parser.parse(f))
        assert len(variants) == 1
        assert variants[0].is_no_call


